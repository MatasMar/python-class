def gcd():
    userInputA = int(input("number a:"))
    userInputB = int(input("number b:"))
    GCD = []
    divisor = userInputA
    print (userInputA / divisor)
    while userInputA > 0:
        if userInputA / divisor == userInputA % divisor and userInputB % 6 == type(int):
            GCD.append(divisor)
